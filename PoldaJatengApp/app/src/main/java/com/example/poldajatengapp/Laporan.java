package com.example.poldajatengapp;

public class Laporan {
    private String Nama,Nim, Pukul, Tgll, Terjadi, Timbul;

    public Laporan() {
    }

    public String getNama() {
        return Nama;
    }

    public void setNama(String nama) {
        Nama = nama;
    }

    public String getNim() {
        return Nim;
    }

    public void setNim(String nim) {
        Nim = nim;
    }

    public String getPukul() {
        return Pukul;
    }

    public void setPukul(String pukul) {
        Pukul = pukul;
    }

    public String getTgll() {
        return Tgll;
    }

    public void setTgll(String tgll) {
        Tgll = tgll;
    }

    public String getTerjadi() {
        return Terjadi;
    }

    public void setTerjadi(String terjadi) {
        Terjadi = terjadi;
    }

    public String getTimbul() {
        return Timbul;
    }

    public void setTimbul(String timbul) {
        Timbul = timbul;
    }
}
